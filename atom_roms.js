function loadcode(codestr,n=0) {

	var i,j,ln,m,st
	st=codestr.split("\n")
	var nb=0
	for (j=0; j<st.length; j++) {
		ln = st[j].split(" ")
		m=str2num("$"+ln[0])+n
		for (i=1; i<ln.length; i++)
		{
			if (ln[i] != "") {
				mem[m+i-1] = str2num("$"+ln[i])  // by passes ROM protection
				nb += 1
			}
		}
	}
	
	mtop = n+nb
	print_mon("Loaded " + hex4(n) + "-" + hex4(mtop-1) +"\n")		
				// set top
	mem[0xd] = (mtop) & 0xff 		//lsb
	mem[0xe] = ((mtop) & 0xff00)>>8 //msb
			
}
	
